#!/usr/bin/env python3
"""
Generate simdbitpacking_u16.c — SIMD bit-packing/unpacking for uint16,
with fused sum aggregation (no output writes during unpack).

Supports 128-bit (AVX) and 256-bit (AVX2) register widths.

Usage:
  python gen_simdbitpacking_u16.py --width 128   # AVX (128-bit registers)
  python gen_simdbitpacking_u16.py --width 256   # AVX2 (256-bit registers)
"""
import argparse
import os
import sys

LANE_BITS = 16
MAX_BIT = 16


def intrinsics(width):
    """Return a dict of intrinsic names for the given register width."""
    if width == 128:
        return dict(
            reg='__m128i',
            load='_mm_loadu_si128',
            store='_mm_storeu_si128',
            setzero='_mm_setzero_si128',
            or_fn='_mm_or_si128',
            and_fn='_mm_and_si128',
            slli='_mm_slli_epi16',
            srli='_mm_srli_epi16',
            set1='_mm_set1_epi16',
            add='_mm_add_epi32',
            unpacklo='_mm_unpacklo_epi16',
            unpackhi='_mm_unpackhi_epi16',
            srli_bytes='_mm_srli_si128',
            extract16='_mm_extract_epi16',
        )
    else:
        return dict(
            reg='__m256i',
            load='_mm256_loadu_si256',
            store='_mm256_storeu_si256',
            setzero='_mm256_setzero_si256',
            or_fn='_mm256_or_si256',
            and_fn='_mm256_and_si256',
            slli='_mm256_slli_epi16',
            srli='_mm256_srli_epi16',
            set1='_mm256_set1_epi16',
            add='_mm256_add_epi32',
            unpacklo='_mm256_unpacklo_epi16',
            unpackhi='_mm256_unpackhi_epi16',
        )


def gen_header(I):
    width = I['width']
    lines = []
    lines.append(f"""\
/**
 * Auto-generated uint16 SIMD bit-packing with fused sum aggregation.
 * Generated by gen_simdbitpacking_u16.py (width={width})
 *
 * This code is released under a BSD License.
 */
#include "simdbitpacking_u16.h"
#include <string.h>
""")

    block = I['block']
    lanes = I['lanes']

    lines.append(f"#define SIMDBlockSize_u16 {block}")
    lines.append("")

    if width == 128:
        lines.append(f"static const {I['reg']} kZero = {{0, 0}};")
    else:
        lines.append(f"static const {I['reg']} kZero = {{0, 0, 0, 0}};")
    lines.append("")

    lines.append(f"static void aggregate_sums_u16({I['reg']} OutReg, {I['reg']}* sum) {{")
    lines.append(f"    /* Zero-extend 8 x uint16 -> 4 x int32, then accumulate.")
    lines.append(f"       _mm_madd_epi16 treats inputs as signed — wrong for values > 32767.")
    lines.append(f"       3 instructions / 8 values = 0.375 inst/val. */")
    lines.append(f"    *sum = {I['add']}(*sum, {I['unpacklo']}(OutReg, kZero));")
    lines.append(f"    *sum = {I['add']}(*sum, {I['unpackhi']}(OutReg, kZero));")
    lines.append("}")
    lines.append("")

    # AVX2-only delta+zigzag helpers, used by simdunpack_u16_delta_{local,carry}.
    if width == 256:
        lines.append("""\
/* ── Delta+ZigZag helpers (AVX2, 16 uint16 lanes) ─────────────────────────── */

static __inline__ uint16_t ZigzagDec16_simdcomp(uint16_t z) {
    return (uint16_t)((z >> 1) ^ (uint16_t)(-(int)(z & 1)));
}

static __inline__ __m256i zigzag_dec_u16_avx2(__m256i x) {
    __m256i odd = _mm256_and_si256(x, _mm256_set1_epi16(1));
    __m256i neg_odd = _mm256_sub_epi16(_mm256_setzero_si256(), odd);
    __m256i half = _mm256_srli_epi16(x, 1);
    return _mm256_xor_si256(half, neg_odd);
}

static __inline__ __m256i prefix_sum_u16_avx2(__m256i x) {
    /* 8-lane Sklansky prefix sum within each 128-bit half (3 levels). */
    x = _mm256_add_epi16(x, _mm256_slli_si256(x, 2));
    x = _mm256_add_epi16(x, _mm256_slli_si256(x, 4));
    x = _mm256_add_epi16(x, _mm256_slli_si256(x, 8));
    /* Broadcast lane 7 (top of low half) into all 8 lanes of high half;
       low half gets zero via -1 byte indices in shuffle_epi8. */
    {
        __m256i lo_in_both = _mm256_permute2x128_si256(x, x, 0x00);
        const __m256i bcast_pattern = _mm256_setr_epi8(
            (char)0x80,(char)0x80,(char)0x80,(char)0x80,
            (char)0x80,(char)0x80,(char)0x80,(char)0x80,
            (char)0x80,(char)0x80,(char)0x80,(char)0x80,
            (char)0x80,(char)0x80,(char)0x80,(char)0x80,
            14, 15, 14, 15, 14, 15, 14, 15,
            14, 15, 14, 15, 14, 15, 14, 15);
        __m256i bcast = _mm256_shuffle_epi8(lo_in_both, bcast_pattern);
        return _mm256_add_epi16(x, bcast);
    }
}

/* LOCAL pipeline: zigzag_dec → per-OutReg prefix sum → aggregate.
   Lane 0 of OutReg is the per-OutReg anchor (zigzag-encoded delta from 0).

   Ablation toggles (define at simdcomp build time to disable that step):
     ABLATE_ZIGZAG_LOCAL    — skip zigzag_dec
     ABLATE_PREFIXSUM_LOCAL — skip prefix_sum
   `aggregate_sums_u16` is always run (per the ablation spec: sum at the end
   after bitpacking completes). */
static __inline__ void agg_pipeline_local_simdcomp(__m256i OutReg, __m256i* sum) {
#ifndef ABLATE_ZIGZAG_LOCAL
    OutReg = zigzag_dec_u16_avx2(OutReg);
#endif
#ifndef ABLATE_PREFIXSUM_LOCAL
    OutReg = prefix_sum_u16_avx2(OutReg);
#endif
    aggregate_sums_u16(OutReg, sum);
}

/* CARRY pipeline: zigzag_dec → prefix_sum → +carry → update carry → aggregate.
   *carry is a scalar uint16_t holding the last decoded value (lane 15 of the
   previous OutReg). Stored scalar to avoid a 32-byte broadcast on every store;
   loaded via _mm256_set1_epi16 (vpbroadcastw) on use.

   Ablation toggles:
     ABLATE_ZIGZAG_CARRY     — skip zigzag_dec
     ABLATE_PREFIXSUM_CARRY  — skip prefix_sum
     ABLATE_CARRY_ADD        — skip the `OutReg + broadcast(*carry)` add
     ABLATE_BROADCAST_LANE15 — skip the scalar carry update (vpextrw lane 15)
                                (use with ABLATE_CARRY_ADD or with a stale *carry
                                 to isolate the extract cost vs. the carry-add)
   `aggregate_sums_u16` is always run.

   Note: with ABLATE_CARRY_ADD or ABLATE_BROADCAST_LANE15 set, decoded results
   are not meaningful — these are timing-only ablations. */
static __inline__ void agg_pipeline_carry_simdcomp(__m256i OutReg, uint16_t* carry,
                                                __m256i* sum) {
#ifndef ABLATE_ZIGZAG_CARRY
    OutReg = zigzag_dec_u16_avx2(OutReg);
#endif
#ifndef ABLATE_PREFIXSUM_CARRY
    OutReg = prefix_sum_u16_avx2(OutReg);
#endif
#ifndef ABLATE_CARRY_ADD
    OutReg = _mm256_add_epi16(OutReg, _mm256_set1_epi16((short)*carry));
#endif
#ifndef ABLATE_BROADCAST_LANE15
    *carry = (uint16_t)_mm256_extract_epi16(OutReg, 15);
#else
    (void)carry;
#endif
    aggregate_sums_u16(OutReg, sum);
}

""")

    return "\n".join(lines)


def gen_null_unpacker(I):
    block = I['block']
    return f"""\
static void SIMD_nullunpacker16(const {I['reg']} *_in, uint16_t *out) {{
  (void)_in;
  memset(out, 0, {block} * sizeof(uint16_t));
}}

"""


def gen_null_packer(I, name_suffix=""):
    return f"""\
static void __SIMD_fastpackwithoutmask0_16{name_suffix}(const uint16_t *_in, {I['reg']} *out) {{
  (void)out;
  (void)_in;
}}

"""


def gen_pack_function(bit, I, name_suffix=""):
    """Generate pack function for given bit width."""
    if bit == 0:
        return gen_null_packer(I, name_suffix)

    lines = []
    lanes = I['lanes']
    block = I['block']
    total_inputs = block // lanes

    lines.append(f"static void __SIMD_fastpackwithoutmask{bit}_16{name_suffix}("
                 f"const uint16_t *_in, {I['reg']} *out) {{")
    lines.append(f"  const {I['reg']} *in = (const {I['reg']} *)(_in);")

    if bit == LANE_BITS:
        lines.append(f"  {I['reg']} InReg = {I['load']}(in);")
        lines.append(f"  {I['store']}(out, InReg);")
        for i in range(1, total_inputs):
            lines.append(f"  InReg = {I['load']}(++in);")
            lines.append(f"  {I['store']}(out + {i}, InReg);")
        lines.append("}")
        lines.append("")
        return "\n".join(lines)

    lines.append(f"  {I['reg']} OutReg;")
    lines.append(f"  {I['reg']} InReg = {I['load']}(in);")
    lines.append("")

    out_word = 0
    bit_pos = 0

    lines.append("  OutReg = InReg;")
    bit_pos = bit

    for i in range(1, total_inputs):
        lines.append(f"  InReg = {I['load']}(++in);")

        remaining = LANE_BITS - bit_pos

        if remaining >= bit:
            if bit_pos == 0:
                lines.append("  OutReg = InReg;")
            else:
                lines.append(f"  OutReg = {I['or_fn']}(OutReg, {I['slli']}(InReg, {bit_pos}));")
            bit_pos += bit
        else:
            if remaining > 0:
                lines.append(f"  OutReg = {I['or_fn']}(OutReg, {I['slli']}(InReg, {bit_pos}));")
            lines.append(f"  {I['store']}(out + {out_word}, OutReg);")
            out_word += 1
            if remaining > 0:
                lines.append(f"  OutReg = {I['srli']}(InReg, {remaining});")
            else:
                lines.append("  OutReg = InReg;")
            bit_pos = bit - remaining

        if bit_pos == LANE_BITS:
            lines.append(f"  {I['store']}(out + {out_word}, OutReg);")
            out_word += 1
            bit_pos = 0

    if bit_pos > 0:
        lines.append(f"  {I['store']}(out + {out_word}, OutReg);")

    lines.append("}")
    lines.append("")
    return "\n".join(lines)


def gen_unpack_function(bit, I, mode='plain', name_suffix=''):
    """Generate fused unpack+sum function for given bit width.

    mode:
      'plain'              — aggregate_sums_u16(OutReg, sum)
      'delta_local'        — zigzag_dec → per-OutReg prefix sum → aggregate
      'delta_carry'        — zigzag_dec → prefix_sum → +carry → update carry → aggregate
      'corrected'          — OutReg += corrections[k] (per-OutReg array); aggregate.
                             Used by FoR-local/hierarchical and PFor corrected paths.
      'corrected_uniform'  — OutReg += anchor (single broadcast for all OutRegs); aggregate.
                             Used by FoR-global: one anchor per 256-elem sub-block.
    """
    assert mode in ('plain', 'delta_local', 'delta_carry', 'corrected', 'corrected_uniform')
    if bit == 0:
        return ""

    lines = []
    lanes = I['lanes']
    block = I['block']
    total_values = block // lanes

    suffix = {
        'plain': '',
        'delta_local': '_delta_local',
        'delta_carry': '_delta_carry',
        'corrected': '_corrected',
        'corrected_uniform': '_corrected_uniform',
    }[mode]
    needs_carry       = mode == 'delta_carry'
    needs_corrections = mode == 'corrected'
    needs_anchor      = mode == 'corrected_uniform'
    needs_outreg      = mode in ('corrected', 'corrected_uniform')

    extra_param = ""
    if needs_carry:
        extra_param = f", uint16_t *carry"
    elif needs_corrections:
        extra_param = f", const {I['reg']} *corrections"
    elif needs_anchor:
        extra_param = f", const {I['reg']} anchor"

    def agg_outreg(v):
        if mode == 'plain':
            return "  aggregate_sums_u16(OutReg, sum);"
        if mode == 'delta_local':
            return "  agg_pipeline_local_simdcomp(OutReg, sum);"
        if mode == 'delta_carry':
            return "  agg_pipeline_carry_simdcomp(OutReg, carry, sum);"
        if mode == 'corrected':
            return (f"  OutReg = _mm256_add_epi16(OutReg, corrections[{v}]);\n"
                    f"  aggregate_sums_u16(OutReg, sum);")
        # corrected_uniform:
        return ("  OutReg = _mm256_add_epi16(OutReg, anchor);\n"
                "  aggregate_sums_u16(OutReg, sum);")

    def agg_inreg(v):
        if mode == 'plain':
            return "  aggregate_sums_u16(InReg, sum);"
        if mode == 'delta_local':
            return "  agg_pipeline_local_simdcomp(InReg, sum);"
        if mode == 'delta_carry':
            return "  agg_pipeline_carry_simdcomp(InReg, carry, sum);"
        if mode == 'corrected':
            return (f"  OutReg = _mm256_add_epi16(InReg, corrections[{v}]);\n"
                    f"  aggregate_sums_u16(OutReg, sum);")
        # corrected_uniform: route through OutReg so we can add anchor.
        return ("  OutReg = _mm256_add_epi16(InReg, anchor);\n"
                "  aggregate_sums_u16(OutReg, sum);")

    lines.append(f"static void __SIMD_fastunpack{bit}_16{suffix}{name_suffix}("
                 f"const {I['reg']} *in, uint16_t *_out, {I['reg']}* sum{extra_param}) {{")
    lines.append("  (void)_out;")
    lines.append(f"  {I['reg']} InReg = {I['load']}(in);")
    lines.append(f"  {I['reg']} OutReg;")

    if bit == LANE_BITS:
        if not needs_outreg:
            lines.pop()  # remove OutReg — not needed for plain/delta modes
        lines.append(agg_inreg(0))
        for i in range(1, total_values):
            lines.append(f"  InReg = {I['load']}(++in);")
            lines.append(agg_inreg(i))
        lines.append("}")
        lines.append("")
        return "\n".join(lines)

    lines.append(f"  const {I['reg']} mask = {I['set1']}((1U << {bit}) - 1);")
    lines.append("")

    bit_pos = 0

    for v in range(total_values):
        remaining = LANE_BITS - bit_pos

        if remaining >= bit:
            if bit_pos == 0:
                lines.append(f"  OutReg = {I['and_fn']}(InReg, mask);")
            else:
                lines.append(f"  OutReg = {I['and_fn']}({I['srli']}(InReg, {bit_pos}), mask);")
            lines.append(agg_outreg(v))
            lines.append("")
            bit_pos += bit
        else:
            if remaining > 0:
                lines.append(f"  OutReg = {I['srli']}(InReg, {bit_pos});")
            lines.append(f"  InReg = {I['load']}(++in);")

            need = bit - remaining
            if remaining > 0:
                lines.append("  OutReg =")
                lines.append(f"      {I['or_fn']}(OutReg, {I['and_fn']}({I['slli']}(InReg, {bit} - {need}), mask));")
            else:
                lines.append(f"  OutReg = {I['and_fn']}(InReg, mask);")
            lines.append(agg_outreg(v))
            lines.append("")
            bit_pos = need

        if bit_pos == LANE_BITS:
            if v < total_values - 1:
                lines.append(f"  InReg = {I['load']}(++in);")
            bit_pos = 0

    lines.append("}")
    lines.append("")
    return "\n".join(lines)


def gen_pack_dispatcher(I):
    lines = []
    lines.append(f"void simdpack_u16(const uint16_t *in, {I['reg']} *out, const uint32_t bit) {{")
    lines.append("  switch (bit) {")
    for b in range(MAX_BIT + 1):
        lines.append(f"  case {b}:")
        lines.append(f"    __SIMD_fastpackwithoutmask{b}_16(in, out);")
        lines.append("    return;")
        lines.append("")
    lines.append("  default:")
    lines.append("    break;")
    lines.append("  }")
    lines.append("}")
    lines.append("")
    return "\n".join(lines)


