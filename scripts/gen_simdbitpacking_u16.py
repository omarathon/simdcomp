def main():
    parser = argparse.ArgumentParser(
        description='Generate SIMD bit-packing for uint16')
    parser.add_argument('--width', type=int, choices=[128, 256], default=128,
                        help='Register width in bits (128=AVX, 256=AVX2)')
    args = parser.parse_args()

    I = intrinsics(args.width)
    lanes = args.width // LANE_BITS
    block = lanes * MAX_BIT  # 128 for 128-bit, 256 for 256-bit
    I['lanes'] = lanes
    I['block'] = block
    I['width'] = args.width

    out = []
    out.append(gen_header(I))
    out.append(gen_null_unpacker(I))

    # Pack functions for all bit widths
    for bit in range(MAX_BIT + 1):
        out.append(gen_pack_function(bit, I))

    out.append(gen_pack_dispatcher(I))

    # Unpack+sum functions for all bit widths
    for bit in range(1, MAX_BIT + 1):
        out.append(gen_unpack_function(bit, I, mode='plain'))

    out.append(gen_unpack_dispatcher(I))

    # AVX2-only: delta-local and delta-carry unpack functions + dispatchers.
    if I['width'] == 256:
        for bit in range(1, MAX_BIT + 1):
            out.append(gen_unpack_function(bit, I, mode='delta_local'))
        out.append(gen_unpack_dispatcher_delta_local(I))

        for bit in range(1, MAX_BIT + 1):
            out.append(gen_unpack_function(bit, I, mode='delta_carry'))
        out.append(gen_unpack_dispatcher_delta_carry(I))

        for bit in range(1, MAX_BIT + 1):
            out.append(gen_unpack_function(bit, I, mode='corrected'))
        out.append(gen_unpack_dispatcher_corrected(I))

        for bit in range(1, MAX_BIT + 1):
            out.append(gen_unpack_function(bit, I, mode='corrected_uniform'))
        out.append(gen_unpack_dispatcher_corrected_uniform(I))

    # Sub-block variants for W=128 (8 OutRegs): pack + corrected_uniform unpack.
    # Only generated for AVX2 (256-bit) since the FoR-global codecs are AVX2-only.
    if I['width'] == 256:
        for W in [32, 64, 128]:
          name_suffix = f'_n{W}'
          I_sub = dict(I, block=W)
          for bit in range(MAX_BIT + 1):
              out.append(gen_pack_function(bit, I_sub, name_suffix=name_suffix))
          for bit in range(1, MAX_BIT + 1):
              out.append(gen_unpack_function(bit, I_sub, mode='corrected_uniform',
                                             name_suffix=name_suffix))
          out.append(gen_pack_dispatcher_nW(I_sub, W, name_suffix))
          out.append(gen_unpack_dispatcher_corrected_uniform_nW(I_sub, W, name_suffix))

    # Short-length handlers
    out.append(gen_shortlength_pack(I))
    out.append(gen_shortlength_unpack(I))

    # Utility functions
    out.append(gen_utility_functions(I))

    script_dir = os.path.dirname(os.path.abspath(__file__))
    src_dir = os.path.join(script_dir, '..', 'src')
    out_path = os.path.join(src_dir, 'simdbitpacking_u16.c')

    with open(out_path, 'w') as f:
        f.write("".join(out))
    print(f"Generated {out_path}")


if __name__ == "__main__":
    main()
