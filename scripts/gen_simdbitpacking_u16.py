    lines.append("    size_t i;")
    lines.append(f"    for (i = 0; i < {outregs}; ++i) {{")
    lines.append(f"      agg_pipeline_carry_simdcomp(_mm256_setzero_si256(), carry, sum);")
    lines.append("    }")
    lines.append("    break;")
    lines.append("  }")
    lines.append("")
    for b in range(1, MAX_BIT + 1):
        lines.append(f"  case {b}:")
        lines.append(f"    __SIMD_fastunpack{b}_16_delta_carry(in, out, sum, carry);")
        lines.append("    break;")
        lines.append("")
    lines.append("  default:")
    lines.append("    break;")
    lines.append("  }")
    lines.append("}")
    lines.append("")
